package com.example.android.utils

fun getAnimalMessage(animal: String): String {
    return when (animal) {
        "dog" -> "강아지상은 귀엽고 친근한 인상이 매력이에요!\n누구에게나 사랑받는 국민 호감 스타일이군요!"
        "cat" -> "고양이상은 도도하고 시크한 매력이 있어요!\n가만히 있어도 아우라가 뿜뿜해요!"
        "bear" -> "곰상은 듬직하고 포근한 느낌이 특징이에요!\n사람들이 자꾸 기대고 싶어 하는 스타일이에요."
        "deer" -> "사슴상은 청순하고 순한 인상이 매력이에요!\n누가 봐도 순정만화 주인공 느낌이군요!"
        "rabbit" -> "토끼상은 귀엽고 깜찍한 매력을 가지고 있어요!\n애교 한 스푼에 모두가 심쿵할지도 몰라요!"
        "wolf" -> "늑대상은 강렬하고 카리스마 넘치는 인상이 돋보여요!\n묘한 매력으로 시선을 끄는 타입이군요."
        "tiger" -> "호랑이상은 당당하고 카리스마 있는 매력이 있어요!\n'리더의 향기'가 나는 포스가 느껴져요!"
        "snake" -> "뱀상은 신비롭고 매혹적인 분위기를 풍겨요!\n한 번 빠지면 헤어나오기 힘든 스타일이에요."
        "squirrel" -> "다람쥐상은 발랄하고 귀여운 매력이 있어요!\n말투나 표정 하나하나에 생기가 넘쳐요!"
        "turtle" -> "거북이상은 차분하고 성실한 느낌이 인상적이에요!\n꾸준함이 당신의 가장 큰 무기랍니다!"
        "dinosaur" -> "공룡상은 독특하고 강렬한 존재감을 가지고 있어요!\n어디서든 한 번 보면 절대 잊을 수 없죠!"
        else -> "당신만의 독특한 매력이 돋보이는 얼굴이에요!\n세상에 단 하나뿐인 분위기를 지녔어요!"
    }
}
